#include "libft.h"

void	ft_bzero(void *p, size_t n)
{
	unsigned char	*v;
	size_t			i;

	v = (unsigned char *)p;
	i = 0;
	while (i < n)
	{
		v[i] = 0;
		i++;
	}
}
